package VASL.build.module.fullrules.ObjectClasses;

public class Scenlisttype {
    private String pScenName;
    private String pScenNum;
    private int pScenID;

    public Scenlisttype() {}

    public String getScenName() {
        return pScenName;
    }

    public void setScenName(String value) {
        pScenName = value;
    }

    public String getScenNum() {
        return pScenNum;
    }

    public void setScenNum(String value) {
        pScenNum = value;
    }

    public int getScenID() {
        return pScenID;
    }

    public void setScenID(int value) {
        pScenID = value;
    }
}